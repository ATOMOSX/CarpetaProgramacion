package seccion5;

public class SentenciaIfElse {
    public static void main(String[] args) {

        float promedio = 5.0f;

        if (promedio >= 5){
            System.out.println("Promedio excelente, felicitaciones");
        }else {
            System.out.println("Promedio mediocre, mejora!");
        }
    }
}
